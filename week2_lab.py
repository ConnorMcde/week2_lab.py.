"""
Python program that converts a temperature from Celsius to Fahrenheit.
"""


Celsius = int(input("Enter the Celsius: "))#represents 25 degrees celsius
Fahrenheit = (Celsius * 9/5) + 32 #formula for conversion having the celsius times by 9/5 then adding 32

print(f"Celsius is {Celsius} and Fahrenheit is {Fahrenheit}.")

"""As Celsius increases so does the fahrenheit"""